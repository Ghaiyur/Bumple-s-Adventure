--==============================================================================================
-- LOAD
--==============================================================================================

function love.load()
    -- Import Windfield
    wf = require 'libraries/windfield/windfield' -- Import
    world = wf.newWorld(0,600) -- Gravity of the physics, zero gravity , direction of gravity
    
    -- This creates a player based collidor
    player = world:newRectangleCollider(360,100,80,80)

    -- Platform -static
    platform = world:newRectangleCollider(250,400,300,100)
    platform:setType('static')
end

--==============================================================================================
-- UPDATE
--==============================================================================================

function love.update(dt)
    world:update(dt)
    
end

--==============================================================================================
-- DRAW
--==============================================================================================

function love.draw()
    world:draw()
    
end

